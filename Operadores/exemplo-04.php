<?php

// Operadores de comparação

$a = 30;
$b = 55;

var_dump($a > $b);
echo "<br/>";

var_dump($a < $b);
echo "<br/>";

//  Igualdade de Valor
var_dump($a == $b);
echo "<br/>";

//  Igualdade de Identidade
var_dump($a === $b);
echo "<br/>";

//  Diferença de Valor
var_dump($a != $b);
echo "<br/>";

//  Diferença de Identidade
var_dump($a !== $b);
echo "<br/>";
